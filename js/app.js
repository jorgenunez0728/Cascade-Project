function emitEvent(name, data) {
    var handlers = _eventBus[name];
    if (!handlers) return;
    for (var i = 0; i < handlers.length; i++) {
        try { handlers[i](data); } catch(e) { console.error('EventBus error on "' + name + '":', e); }
    }
}

