function nowLocalDatetimeValue() {
  const d = new Date();
  // Ajuste para que datetime-local muestre hora local correcta
  d.setMinutes(d.getMinutes() - d.getTimezoneOffset());
  return d.toISOString().slice(0,16); // "YYYY-MM-DDTHH:MM"
}


